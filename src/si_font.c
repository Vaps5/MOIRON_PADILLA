char *si_font_alphanum_get(char c, int *width)
{
  *width = 5;
  /*Si j'ai bien compris, sert à utiliser la largeur des caractères lors de l'appel .*/
  
  /* 3 cas: 'c' est une lettre majuscule ou un chiifre ou un caractere spécial */
  if(c >= 'A' && c <= 'Z')
    {
      int i = c-'A';
      return (char *)si_font_alphanum[i]; /* correspondance char/int avec ASCII */
    }

  if(c >= '0' && c <= '9')
    {
      int i = c - '0' + 26;
      return (char *)si_font_alphanum[i]; /* 26 car c'est card(alphabet) */
    }
  else
    {
      switch(c) /* on distingue les cas restants */
	{
            case ' ':  return (char *)si_font_alphanum[36];
            case '<':  return (char *)si_font_alphanum[37];
            case '>':  return (char *)si_font_alphanum[38];
            case '-':  return (char *)si_font_alphanum[39];
            case '*':  return (char *)si_font_alphanum[40];
            case '?':  return (char *)si_font_alphanum[41];
            case '=':  return (char *)si_font_alphanum[42];
	}
    }
  return (char *)si_font_alphanum[36]; /* s'il sait pas, il renvoie un espace */
}
